 * │ useNoise    │ bool (true)      │ 是否添加杂点                           │
 * │ imageH      │ int (0=自动)     │ 验证码图片高度(px)                      │
 * │ imageW      │ int (0=自动)     │ 验证码图片宽度(px)                      │
 * │ length      │ int (5)          │ 验证码位数                             │
 * │ fontttf     │ string ('')      │ 验证码字体(空=随机)                     │
 * │ bg          │ array            │ 背景颜色 RGB [243,251,254]            │
 * │ reset       │ bool (true)      │ 验证成功后是否重置                      │
 * │ secure      │ bool (false)     │ 是否开启安全模式                        │
 * └─────────────┴──────────────────┴─────────────────────────────────────┘
 *
 * 【安全建议】
 * - 生产环境建议启用 useCurve 和 useNoise 增加识别难度
 * - 建议使用字母+数字混合的 codeSet 提高安全性
 * - expire 不宜设置过长，防止暴力破解
 *
 * ============================================================
 */

