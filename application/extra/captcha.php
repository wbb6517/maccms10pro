 * │   └── helper.php         : 辅助函数 (captcha_check等)
 * └── assets/
 *     └── ttfs/              : 字体文件目录
 *
 * 【完整配置项说明】
 *
 * ┌─────────────┬──────────────────┬─────────────────────────────────────┐
 * │ 配置项       │ 类型/默认值       │ 说明                                  │
